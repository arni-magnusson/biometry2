#' Subtract y from x
#'
#' This function subtracts y from x.
#'
#' @param x a number.
#' @param y a number.
#'
#' @return
#' A number, value of x subtracted by the value of y.
#'
#' @author
#' Sebastien Matlosz
#'
#' @seealso
#' \link{Arithmetic}, \code{\link{twice}}
#'
#' @examples
#' subtract(10,8)
#'
#' subtract(pi, (pi+1))
#'
#' @export

subtract <- function(x,y)
{
  x-y
}

