#' Divide by Two
#'
#' This function divides x by 2.
#'
#' @param x a number.
#'
#' @return
#' A number, half the value of x.
#'
#' @author
#' Ragnhildur Gudmundsdottir
#'
#' @seealso 
#' \link{Arithmetic}, \code{\link{twice}}.
#' 
#' @examples
#' half(5)
#'  
#' half(pi)
#'  
#' @export  
    
half <- function(x)
{
  x/2
}
