#' Multiply by Two
#'
#' This function multiplies x by 2.
#'
#' @param x a number.
#'
#' @return
#' A number, two times the value of x.
#'
#' @author
#' Arni Magnusson
#'
#' @seealso
#' \link{Arithmetic}
#'
#' @examples
#' twice(10)
#'
#' twice(pi)
#'
#' @export

twice <- function(x)
{
  2*x
}
